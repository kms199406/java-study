package valiable;

public class Var2 {
    public static void main(String[] args) {
        int a;//변수 선언
        a = 1;// 초기화
        System.out.println(a);

        int b = 5;
        System.out.println(b);

        int c = 5, d = 6;
        System.out.println(c + d);
        System.out.println(d);
    }
}
